package scala.tutorial.basic_examples

object ExpressionsMain {

  def main(args: Array[String]): Unit = {
    println(1 + 1) //2
    println(2 - 1) //1
    println(2 * 2) //4
    println(4 / 2) //2
    println(3 / 2) //1
    println("Show me a string expression") //Show me a string expression
    println("string expression concat with " + "another string expression") //string expression concat with another string expression
  }

}
